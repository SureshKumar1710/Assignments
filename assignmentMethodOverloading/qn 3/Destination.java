class Destination // qn 3
{
    private String name;
    private int inhabitants;

    public void setName(String name) 
    {
        this.name = name;
    }

    public void setInhabitants(int inhabitants) 
    {
        this.inhabitants = inhabitants;
    }

    public String getName() 
    {
        return name;
    }

    public int getInhabitants() 
    {
        return inhabitants;
    }
}
